    private void setPatientsOnMenu(){
        
        this.getSelectionMenu().removeAllItems();
        this.getSelectionMenu().addItem("Select a Patient");
        
        for (int i = 0; i < this.physician.getPatientList().getPatientObjectList().size(); i++){
            
            this.getSelectionMenu().addItem(this.physician.getPatientList().getPatientObjectList().get(i).getFullName()); 
            
        }
        
    }
    
